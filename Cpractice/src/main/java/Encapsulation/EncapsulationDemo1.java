package Encapsulation;

public class EncapsulationDemo1 {
private String name = "vanitha";
private int id;
private double d;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public double getD() {
	return d;
}
public void setD(double d) {
	this.d = d;
}



}