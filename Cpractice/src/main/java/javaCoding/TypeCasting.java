package javaCoding;

public class TypeCasting {

	public static void main(String[] args) {
		// downcasting or widening typecasting
		
		byte b=1;
		short s=b;
		System.out.println(s);
		
		int n=4;
		long l=n;
		System.out.println(l);
		
		// upcasting or narrowing 
		
		float f=10.5f;
	long g=(long)f;
	System.out.println(g);
		
		

	}

}
