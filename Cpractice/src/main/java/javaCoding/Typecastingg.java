package javaCoding;

public class Typecastingg {

	public static void main(String[] args) {
// widening (converting lower data type to higher data type)
		
		
		int a=9;
		long l=a;
		System.out.println(l);
		
		
		// narrowing (converting higher data type to lower data type)
		
		double d= 29.0;
		int i = (int) d;
		System.out.println(i);
		

	}

}
